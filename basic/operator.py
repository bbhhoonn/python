# 산술 연산
a=10 
b=3

print(a+b)
print(a-b)
print(a/b) # float
print(a//b) # int
print(a*b)
print(a**b)
print(a%b)

# 비교 연산

print(a>b)
print(a<=b)
print(a==b)
print(a!=b)

# 논리 연산

t=True
f=False
print(t and f)
print(t or f)
print(not f)

# 할당 연산

a = a + 10
print(a)
a += 10
print(a)

# bit 연산

a=40
